        /// <summary>
        /// 检查身份证生日
        /// </summary>
        /// <param name="strIdcard">要判断的idcard字符串</param>
        /// <returns>判断结果</returns>
        public static bool IsValidIdCardBirthday(string strIdcard, int beginAge, int endAge)
        {
            var len = strIdcard.Length;
            //身份证15位时，次序为省（3位）市（3位）年（2位）月（2位）日（2位）校验位（3位），皆为数字
            if (len == 15)
            {
                var re_fifteen = @"^(\d{6})(\d{2})(\d{2})(\d{2})(\d{3})$";
                var arr_data = Regex.Match(strIdcard, re_fifteen);
                var year = arr_data.Groups[2].Value;
                var month = arr_data.Groups[3].Value;
                var day = arr_data.Groups[4].Value;
                var birthday = new DateTime(Convert.ToInt32("19" + year), Convert.ToInt32(month), Convert.ToInt32(day));
                return verifyBirthday(Convert.ToInt32("19" + year), Convert.ToInt32(month), Convert.ToInt32(day), birthday, beginAge, endAge);
            }
            //身份证18位时，次序为省（3位）市（3位）年（4位）月（2位）日（2位）校验位（4位），校验位末尾可能为X
            if (len == 18)
            {
                var re_eighteen = @"^(\d{6})(\d{4})(\d{2})(\d{2})(\d{3})([0-9]|X)$";
                var arr_data = Regex.Match(strIdcard, re_eighteen);
                var year = arr_data.Groups[2].Value;
                var month = arr_data.Groups[3].Value;
                var day = arr_data.Groups[4].Value;
                var birthday = new DateTime(Convert.ToInt32(year), Convert.ToInt32(month), Convert.ToInt32(day));
                return verifyBirthday(Convert.ToInt32(year), Convert.ToInt32(month), Convert.ToInt32(day), birthday, beginAge, endAge);
            }
            return false;
        }

        /// <summary>
        /// 校验身份证日期
        /// </summary>
        /// <param name="year"></param>
        /// <param name="month"></param>
        /// <param name="day"></param>
        /// <param name="birthday"></param>
        /// <param name="beginAge"></param>
        /// <param name="endAge"></param>
        /// <returns></returns>
        static bool verifyBirthday(int year, int month, int day, DateTime birthday, int beginAge, int endAge)
        {
            var now_year = DateTime.Now.Year;
            var now_day = DateTime.Now.Day;
            var now_month = DateTime.Now.Month;
            //年月日是否合理
            if (birthday.Year == year && birthday.Month == month && birthday.Day == day)
            {
                //判断年份的范围（3岁到100岁之间)
                var time = now_year - year;
                if (time > beginAge && time < endAge)
                {
                    return true;
                }
                if (time == endAge)
                {
                    time = month - now_month;
                    if (time > 0)
                    {
                        return true;
                    }
                    if (time == 0)
                    {
                        time = day - now_day;
                        if (time >= 5)
                        {
                            return true;
                        }
                    }
                }

                if (time == beginAge)
                {
                    time = now_month - month;
                    if (time > 0)
                    {
                        return true;
                    }
                    if (time == 0)
                    {
                        time = now_day - day;
                        if (time >= 0)
                        {
                            return true;
                        }
                    }
                }
            }
            return false;
        }

