        }

        /// <summary>
        /// 15位转18位身份证号
        /// </summary>
        /// <param name="strIdcard"></param>
        /// <returns></returns>
        static string changeFivteenToEighteen(string strIdcard)
        {
            if (strIdcard.Length == 15)
            {
                var arrInt = new[] { 7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2 };
                var arrCh = new[] { '1', '0', 'X', '9', '8', '7', '6', '5', '4', '3', '2' };
                var cardTemp = 0;
                strIdcard = strIdcard.Substring(0, 6) + "19" + strIdcard.Substring(6, strIdcard.Length - 6);
                for (int i = 0; i < 17; i++)
                {
                    cardTemp += Convert.ToInt32(strIdcard.Substring(i, 1)) * arrInt[i];
                }
                strIdcard += arrCh[cardTemp % 11];
                return strIdcard;
            }
            return strIdcard;
        }

